package hr.fer.zemris.java.gui.layouts;


/**
 * Klasa sprema koordinate neke komponente.
 * @author Mateo
 *
 */
public class RCPosition {
	/**
	 * x koordinata
	 */
	public int x;
	/**
	 * y koordinata
	 */
	public int y;
	
	/**
	 * Konstruktor prima koordinate.
	 * @param x
	 * @param y
	 */
	public RCPosition(int x,int y){
		this.x=x;
		this.y=y;
	}
}
